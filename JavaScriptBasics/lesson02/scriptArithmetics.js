var a = 6;
var b = 2;

// Adiction operator
console.log(a + b);

// Subtraction operator
console.log(a - b);

// Multiplication operator
console.log(a * b);

// Integer division operator
console.log(a / b);

// Power operator
console.log(a ** b);

// Modulus (Remainder) operator
console.log(a % b);

// Increment operator
console.log("Before: " + ++a + " After: " + b++);

// Decrincrement operator
console.log("Before: " + --a + " After: " + b--);
