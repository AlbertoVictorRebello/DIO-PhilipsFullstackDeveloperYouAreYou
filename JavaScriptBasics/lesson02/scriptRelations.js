var a = 6;
var b = 2;

// Greater than
console.log(a > b); // true

// Greater or equal than
console.log(a >= b); // true

// Lower than
console.log(a < b); // false

// Lower or equal than
console.log(a <= b); // false

// equal value
var comparing = "0" == 0;
console.log(comparing); // true

// equal value and equal type
var comparing = "0" === 0;
console.log(comparing); // false